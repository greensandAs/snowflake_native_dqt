-- DQ Framework: baseline DDL for all metadata tables (schemachange versioned, DDL only)
-- Co-authored with CoCo
CREATE DATABASE IF NOT EXISTS DQ_FRAMEWORK;

USE DATABASE DQ_FRAMEWORK;

CREATE SCHEMA IF NOT EXISTS METADATA;
-- DQ_ERRORS holds the per-dataset <DATASET>_DQ_FAILURE tables created at runtime by handlers
CREATE SCHEMA IF NOT EXISTS DQ_ERRORS;

USE SCHEMA METADATA;

-- Sequence used to allocate DATASET_RUN_ID values (individual dataset runs)
CREATE SEQUENCE IF NOT EXISTS RUN_ID_SEQ START WITH 1 INCREMENT BY 1 ORDER;

-- Sequence used to allocate BATCH_ID values (project-level correlation across dataset runs)
CREATE SEQUENCE IF NOT EXISTS BATCH_ID_SEQ START WITH 1000 INCREMENT BY 1 ORDER;

CREATE TABLE IF NOT EXISTS DQ_DATASET (
    DATASET_ID NUMBER(38, 0),
    PROJECT_ID NUMBER(38, 0),
    DATASET_TYPE VARCHAR(16777216),
    TABLE_ID NUMBER(38, 0),
    DATASET_NAME VARCHAR(16777216) COLLATE 'en-ci',
    PROJECT_NAME VARCHAR(16777216) COLLATE 'en-ci',
    DATABASE_NAME VARCHAR(16777216) COLLATE 'en-ci',
    SCHEMA_NAME VARCHAR(16777216) COLLATE 'en-ci',
    TABLE_NAME VARCHAR(16777216) COLLATE 'en-ci',
    CUSTOM_SQL VARCHAR(16777216) COLLATE 'en-ci',
    PRIMARY_KEY_COLUMNS VARCHAR(16777216),
    CANDIDATE_KEY_COLUMNS VARCHAR(16777216),
    DATASET_DESCRIPTION VARCHAR(16777216),
    CREATED_BY VARCHAR(16777216),
    CREATED_TIMESTAMP TIMESTAMP_NTZ(9),
    DATASIZE VARCHAR(255),
    LAYER VARCHAR(50),
    IS_INCREMENTAL BOOLEAN,
    FILTER_COND VARCHAR(16777216),
    SELECTED_COLUMNS VARCHAR(16777216),
    FILTER_CONDITION VARCHAR(16777216),
    LIMIT_ROWS VARCHAR(16777216),
    INCR_DATE_COLUMN_1 VARCHAR(16777216),
    INCR_DATE_COLUMN_2 VARCHAR(16777216),
    DQ_RESULTS_FORMAT VARCHAR(16777216),
    USER_ID NUMBER(38, 0),
    SESSION_ID NUMBER(38, 0),
    JUSTIFICATION VARCHAR(255),
    THRESHOLD NUMBER(5, 2),
    REF_BLOB_PATH VARCHAR(16777216),
    CONSTRAINT PK_DQ_DATASET PRIMARY KEY (DATASET_ID)
);

